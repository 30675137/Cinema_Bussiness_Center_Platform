package com.cinema.material.domain;

/**
 * M001: 物料分类枚举
 * 
 * @author Cinema System
 * @since 2026-01-14
 */
public enum MaterialCategory {
    /**
     * 原料 - 用于生产/调制的基础材料
     * 例如: 朗姆酒, 可乐糖浆, 玉米粒, 黄油
     */
    RAW_MATERIAL,
    
    /**
     * 包材 - 用于包装的消耗品
     * 例如: 杯子, 吸管, 纸袋, 爆米花桶
     */
    PACKAGING
}
